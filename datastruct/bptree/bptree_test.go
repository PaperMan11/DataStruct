package bptree

import (
	"fmt"
	"testing"
)

func TestBPTree(t *testing.T) {
	tree := NewBPTree(5)
	tree.Insert(39, 39)
	tree.Insert(22, 22)
	tree.Insert(97, 97)
	tree.Insert(41, 41)
	tree.Insert(53, 53)
	tree.Insert(13, 13)
	tree.Insert(21, 21)
	tree.Insert(40, 40)
	tree.Insert(30, 30)
	tree.Insert(27, 27)
	tree.Insert(33, 33)
	tree.Insert(36, 36)
	tree.Insert(35, 35)
	tree.Insert(34, 34)
	tree.Insert(24, 24)
	tree.Insert(29, 29)
	tree.Insert(26, 26)
	tree.Insert(17, 17)
	tree.Insert(28, 28)
	tree.Insert(23, 23)
	tree.Insert(31, 31)
	tree.Insert(32, 32)
	tree.printInLog()

	fmt.Println("--------------删除后-----------------")
	tree.Delete(39)
	tree.Delete(22)
	tree.Delete(97)
	tree.Delete(41)
	tree.Delete(53)
	tree.Delete(13)
	tree.Delete(21)
	tree.Delete(40)
	tree.Delete(30)
	tree.Delete(27)
	tree.Delete(33)
	tree.Delete(36)
	tree.Delete(35)
	tree.Delete(34)
	tree.Delete(24)
	tree.Delete(29)
	tree.Delete(26)
	tree.Delete(17)
	tree.Delete(28)
	tree.Delete(23)
	tree.Delete(31)
	tree.Delete(32)
	tree.printInLog()
	fmt.Println("--------------添加后-----------------")
	tree.Insert(39, 39)
	tree.Insert(39, -1)
	tree.Insert(22, 22)
	tree.Insert(97, 97)
	tree.printInLog()
}
